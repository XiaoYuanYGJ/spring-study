package com.javaketang.entity;

import org.apache.ibatis.annotations.Param;

/**
 * 成绩
 * 
 * @author yuanguangjie
 *
 */
public class StudentCJ {

	private Integer cjId;
	private Integer stdId; // 学生编号
	private Integer kcId; // 课程编号
	private double pingfen;
	private String text;

	public StudentCJ() {

	}

	public StudentCJ(Integer cjId, Integer stdId, Integer kcId, double pingfen, String text) {

		this.cjId = cjId;
		this.stdId = stdId;
		this.kcId = kcId;
		this.pingfen = pingfen;
		this.text = text;
	}

	public Integer getCjId() {
		return cjId;
	}

	public Integer getKcId() {
		return kcId;
	}

	public double getPingfen() {
		return pingfen;
	}

	public Integer getStdId() {
		return stdId;
	}

	public String getText() {
		return text;
	}

	public void setCjId(Integer cjId) {
		this.cjId = cjId;
	}

	public void setKcId(Integer kcId) {
		this.kcId = kcId;
	}

	public void setPingfen(double pingfen) {
		this.pingfen = pingfen;
	}

	public void setStdId(Integer stdId) {
		this.stdId = stdId;
	}

	public void setText(String text) {
		this.text = text;
	}

}
